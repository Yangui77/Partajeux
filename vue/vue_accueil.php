<h3>Partajeux<br>
<br>
Est une plateforme de mise en relation qui permet aux utilisateurs d'échanger pour une durée de temps leurs jeux video en ligne.
Ils déterminent grâce à l'application les jeux qu'ils veulent échanger, puis fixent un rendez-vous (durée, date, heure, lieu de l'échange).
L'utilisateur peut se connecter à l'application, s'identifier, ajouter ses jeux à échanger et ses jeux voulu.
De plus quand l'utilisateur recherche des jeux qu'il veut, l'application affichera les utilisateurs qui peuvent potentiellement échanger (Ils sont intéressés par un de ses jeux, ou ont la console pour un de ses jeux).
<br>
L'utilisateur peut ensuite proposer un échange et doit attendre la confirmation de l'autre utilisateur. 
Lorsque l'échange s'effectue les utilisateurs confirment l'échange et les jeux deviennent indisponibles.
Au moment quand la durée de l'échange est terminée les utilisateurs doivent confirmer le retour des jeux pour qu'ils redeviennent disponibles dans l'application. 
